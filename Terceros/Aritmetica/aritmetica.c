
#include "aritmetica.h"

/////////////////////////////////////////////////////////////////////////////////////////////////
numero_t suma (numero_t a,numero_t b)
{
	return a + b;
}
/////////////////////////////////////////////////////////////////////////////////////////////////
numero_t resta (numero_t a,numero_t b)
{
	return a - b;
}
/////////////////////////////////////////////////////////////////////////////////////////////////
numero_t multiplicacion (numero_t a,numero_t b)
{
	return a * b;
}
/////////////////////////////////////////////////////////////////////////////////////////////////
numero_t division (numero_t a,numero_t b)
{
	return a / b;
}
/////////////////////////////////////////////////////////////////////////////////////////////////





